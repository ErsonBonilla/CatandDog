    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
        FileNameExtensionFilter filtrado = new FileNameExtensionFilter("JPG, PNG & GIF", "jpg", "png", "gif");
        jFileChooser.setFileFilter(filtrado);
        int respuesta = jFileChooser.showOpenDialog(this);
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            String ruta = jFileChooser.getSelectedFile().getPath();
            imagenSeleccionada = new ImageIcon(ruta).getImage();
            ImageIcon mIcono = new ImageIcon(imagenSeleccionada.getScaledInstance(imagenMascota.getWidth(), imagenMascota.getHeight(), Image.SCALE_SMOOTH));
            imagenMascota.setIcon(mIcono);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

