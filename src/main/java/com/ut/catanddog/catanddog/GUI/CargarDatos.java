    private byte[] obtenerDatosImagen() throws IOException {
        if (imagenSeleccionada != null) {
            BufferedImage bufferedImage = new BufferedImage(imagenSeleccionada.getWidth(null), imagenSeleccionada.getHeight(null), BufferedImage.TYPE_INT_RGB);
            Graphics2D g2d = bufferedImage.createGraphics();
            g2d.drawImage(imagenSeleccionada, 0, 0, null);
            g2d.dispose();

            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            ImageIO.write(bufferedImage, "jpg", baos);
            baos.flush();
            byte[] imageInByte = baos.toByteArray();
            baos.close();

            return imageInByte;
        }
        return null;
    }
